#include <unistd.h>

int	main(void)
{
	char	c1;
	char	c2;
	char	c3;

	c1 = '0';
	c2 = '0';
	c3 = '0';
	while (c1 <= '9')
	{
		while (c2 <= '9')
		{
			while (c3 <= '9')
			{
				write(1, &c1, 1);
				write(1, &c2, 1);
				write(1, &c3, 1);
				write(1, "\n", 1);
				c3++;
			}
			c3 = '0';
			c2++;
		}
		c2 = '0';
		c1++;
	}
}
